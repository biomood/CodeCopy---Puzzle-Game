-- return true if level is complete
function levelComplete(level)
  -- iterate through the current game maze and see if it matches the desired level
  for i,c in ipairs(levels[level].start) do
    if (levels[level].maze[i] ~= c) then
      return false
    end
  end
  
  return true
end

